def send_async_email(app, msg_data):
    """‡∏™‡πà‡∏á‡∏≠‡∏µ‡πÄ‡∏°‡∏•‡πÉ‡∏ô Background Thread"""
    with app.app_context():
        try:
            # ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤‡∏°‡∏µ Email config ‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà
            if not MAIL_USERNAME or not MAIL_PASSWORD:
                print("‚ùå Email credentials not configured")
                return
            
            # ‡∏•‡∏≠‡∏á‡πÉ‡∏ä‡πâ SendGrid ‡∏Å‡πà‡∏≠‡∏ô (‡∏ñ‡πâ‡∏≤‡∏°‡∏µ API Key ‡πÅ‡∏•‡∏∞‡∏ï‡∏¥‡∏î‡∏ï‡∏±‡πâ‡∏á‡πÅ‡∏•‡πâ‡∏ß)
            if SENDGRID_AVAILABLE and SENDGRID_API_KEY:
                try:
                    message = Mail(
                        from_email=MAIL_USERNAME,
                        to_emails=msg_data['to'],
                        subject=msg_data['subject'],
                        html_content=msg_data['html']
                    )
                    sg = SendGridAPIClient(SENDGRID_API_KEY)
                    response = sg.send(message)
                    print(f"‚úÖ Email sent via SendGrid (status: {response.status_code})")
                    return
                except Exception as e:
                    print(f"‚ö†Ô∏è SendGrid failed, falling back to Gmail SMTP: {e}")
            
            # ‡πÉ‡∏ä‡πâ Gmail SMTP
            msg = MIMEMultipart('alternative')
            msg['Subject'] = msg_data['subject']
            msg['From'] = MAIL_USERNAME
            msg['To'] = msg_data['to']
            
            part = MIMEText(msg_data['html'], 'html')
            msg.attach(part)
            
            with smtplib.SMTP('smtp.gmail.com', 587, timeout=15) as server:
                server.starttls()
                server.login(MAIL_USERNAME, MAIL_PASSWORD)
                server.send_message(msg)
            print("‚úÖ Email sent via Gmail SMTP")
        except Exception as e:
            print(f"‚ùå Error sending email: {e}")
            # ‡πÑ‡∏°‡πà raise exception ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÑ‡∏°‡πà‡πÉ‡∏´‡πâ Thread crash

# ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏™‡πà‡∏á‡∏≠‡∏µ‡πÄ‡∏°‡∏•
def send_email(subject, recipient, html_content):
    """‡∏™‡∏£‡πâ‡∏≤‡∏á‡πÅ‡∏•‡∏∞‡∏™‡πà‡∏á‡∏≠‡∏µ‡πÄ‡∏°‡∏•‡πÅ‡∏ö‡∏ö Async"""
    msg_data = {
        'subject': subject,
        'to': recipient,
        'html': html_content
    }
    
    # ‡∏™‡πà‡∏á‡∏≠‡∏µ‡πÄ‡∏°‡∏•‡πÉ‡∏ô Background Thread
    Thread(target=send_async_email, args=(app, msg_data)).start()
    return True

